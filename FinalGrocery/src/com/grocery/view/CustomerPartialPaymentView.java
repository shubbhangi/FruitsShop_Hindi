    private void customerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customerActionPerformed
        if(customer.getItemCount() > 0)
        {
            address.setText("N/A");
            contact.setText("N/A");
            gstNumber.setText("N/A");
            jPanelCustomer.setVisible(false);
            newCustomer.setText("");
            jLabelDues.setText("0.00");
             
            if(customer.getSelectedItem().toString().equalsIgnoreCase("add new"))
                jPanelCustomer.setVisible(true);
            else
            {
                CustomerMaster customerMaster = new CustomerMaster();
                customerMaster.setName(customer.getSelectedItem().toString());
                getDetails(customerMaster);
            }
            
        }
    }//GEN-LAST:event_customerActionPerformed

