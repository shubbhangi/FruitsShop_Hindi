    private void getDetails(CustomerMaster customerMaster) 
    {
        
        CustomerMasterQuery customerMasterQuery = new CustomerMasterQuery();
        
        List<CustomerMaster> list = customerMasterQuery.getCustomer(customerMaster);
        
        for(CustomerMaster cm: list)
        {
            contact.setText(cm.getContact());
            address.setText(cm.getAddress());
            jLabelDues.setText(String.valueOf(cm.getBalance()));
            gstNumber.setText(cm.getGstNumber());
        }
    }

    private void loadCustomer(CustomerMaster customerMaster) 
    {
        CustomerMasterQuery customerMasterQuery = new CustomerMasterQuery();
        DefaultComboBoxModel defaultComboBoxModel = (DefaultComboBoxModel)customer.getModel();
        defaultComboBoxModel.removeAllElements();
        
        defaultComboBoxModel.addElement("--Select--");
        defaultComboBoxModel.addElement("Add New");
        List<CustomerMaster> list = customerMasterQuery.getCustomer(customerMaster);
        
        for(CustomerMaster cm: list)
            defaultComboBoxModel.addElement(cm.getName());
        
        customer.setModel(defaultComboBoxModel);
    }

    private void pay() 
    {
        CustomerMaster customerMaster = new CustomerMaster();
        CustomerMasterQuery customerMasterQuery = new CustomerMasterQuery();
        
        if(paidAmount.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, MessageFormat.getMessage("Please enter the Paid Amount"), "Error Message", JOptionPane.ERROR_MESSAGE);
            paidAmount.requestFocus();
            return;
        }
        
        if(!(paymentMode.getText().trim().equalsIgnoreCase("cash")))
        {
            if(Float.parseFloat(paidAmount.getText().trim()) == 0)
            {
                JOptionPane.showMessageDialog(null, MessageFormat.getMessage("Paid Amount can't be Zero for Cheque/Card"), "Error Message", JOptionPane.ERROR_MESSAGE);
                paidAmount.requestFocus();
                return;
            }
        }
        
        String customerName = customer.getSelectedItem().toString();
        
        if(customerName.equalsIgnoreCase("--select--"))
        {
            JOptionPane.showMessageDialog(null, MessageFormat.getMessage("Please select a Customer"), "Error Message", JOptionPane.ERROR_MESSAGE);
            customer.requestFocus();
            return;
        }
        
        if(customerName.equalsIgnoreCase("add new"))
        {
            if(newCustomer.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, MessageFormat.getMessage("Please enter the Customer name"), "Error Message", JOptionPane.ERROR_MESSAGE);
                newCustomer.requestFocus();
                return;
            }
            
            customerName = newCustomer.getText().trim();
            
            customerMaster.setName(customerName);
            
            List<CustomerMaster> customerList = customerMasterQuery.getCustomer(customerMaster);
            if(!(customerList.isEmpty()))
            {
                JOptionPane.showMessageDialog(null, MessageFormat.getMessage("This Customer name already exists"), "Error Message", JOptionPane.ERROR_MESSAGE);
                newCustomer.requestFocus();
                return;
            }
        }
        
        int confirm = JOptionPane.showConfirmDialog(null, MessageFormat.getMessage("Are you sure of saving these details?"));
        if(confirm == JOptionPane.YES_OPTION)
        {
            customerMaster.setName(customerName);
            customerMaster.setContact(contact.getText().trim());
            customerMaster.setAddress(address.getText().trim());
            customerMaster.setGstNumber(gstNumber.getText().trim());
            
            List<CustomerMaster> customerList = customerMasterQuery.getCustomer(customerMaster);

            if(customerList.isEmpty())
            {
                customerMaster.setBalance(new BigDecimal(0));
                customerMasterQuery.insertIntoCustomerMaster(customerMaster);
            }
            else
            {
                for(CustomerMaster cm: customerList)
                {
                   customerMaster.setId(cm.getId());
                   customerMaster.setBalance(cm.getBalance());
                }

                customerMasterQuery.updateCustomerMaster(customerMaster);
            }
            
            String paymentMode = this.paymentMode.getText();
            
            SaleMaster saleMaster = new SaleMaster();
            SaleMasterQuery saleMasterQuery = new SaleMasterQuery();
            
            CustomerPartialPayment customerPartialPayment = new CustomerPartialPayment();
            CustomerPartialPaymentQuery customerPartialPaymentQuery = new CustomerPartialPaymentQuery();
            
            customerPartialPayment.setPaymentMode(paymentMode);
            customerPartialPayment.setBank(bank.getText());
            customerPartialPayment.setChequeOrCardNumber(number.getText());
            customerPartialPayment.setCustomerMaster(customerMaster);
            customerPartialPayment.setDate(saleDate.getDate());
            
            if(!(paymentMode.equalsIgnoreCase("cheque")))
                customerPartialPayment.setStatus("1");
            else
                customerPartialPayment.setStatus("0");
            customerPartialPayment.setPaidAmount(new BigDecimal(paidAmount.getText().trim()));
            
            if(chequeDate.getDate() != null)
                customerPartialPayment.setChequeDate(chequeDate.getDate());
            
            saleMaster.setDiscount(new BigDecimal(discount.getText().trim()));
            saleMaster.setFinalBillAmount(new BigDecimal(finalAmount.getText().trim()));
            saleMaster.setBillAmount(new BigDecimal(billAmount.getText()));
            saleMaster.setId(Integer.parseInt(billId.getText()));
            saleMaster.setCustomerMaster(customerMaster);
            saleMaster.setStatus("1");
            saleMaster.setReference(reference.getText().trim());
            
            if(remark.getText().equalsIgnoreCase("gst taken"))
                saleMaster.setRemark("GST Taken");
            else
                saleMaster.setRemark("GST Not Taken");
            saleMasterQuery.updateSaleMaster(saleMaster);
                
            customerPartialPaymentQuery.insertIntoCustomerPartialPayment(customerPartialPayment);
            
            if(paymentMode.equalsIgnoreCase("cheque"))
              customerMaster.setBalance(customerMaster.getBalance().add(new BigDecimal(finalAmount.getText())));
            else
                customerMaster.setBalance((customerMaster.getBalance().add(new BigDecimal(finalAmount.getText()))).subtract(new BigDecimal(paidAmount.getText())));
            
            customerMasterQuery.updateCustomerBalance(customerMaster);
            JOptionPane.showMessageDialog(null, MessageFormat.getMessage("Sale successful"));
            String remark = this.remark.getText().trim();
            
            dispose();
            
            SalesPageView storeDetails = new SalesPageView(jDesktopPane);
            storeDetails.setTitle("Sales Page");
            jDesktopPane.add(storeDetails);
            SetDimension setDimension = new SetDimension();
            setDimension.setInternalFrameLocation(jDesktopPane, storeDetails);
            storeDetails.setVisible(true);
            
            if(remark.equalsIgnoreCase("gst taken"))
                printBill(saleMaster, true);
            else
                printBill(saleMaster, false);
        }   
    }
    
