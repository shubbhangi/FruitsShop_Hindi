    private void newCustomerKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_newCustomerKeyTyped
        char c = evt.getKeyChar();
        if(!(Character.isAlphabetic(c) ||  (c == KeyEvent.VK_BACK_SPACE)|| (c == KeyEvent.VK_DELETE) || (c == KeyEvent.VK_SPACE) || c == '.' || c == '-'))
        evt.consume();
    }//GEN-LAST:event_newCustomerKeyTyped

