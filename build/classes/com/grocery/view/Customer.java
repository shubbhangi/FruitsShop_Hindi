    private void deleteWarehouse() {
        WarehoseDetailsQuery123 warehoseDetailsQuery = new WarehoseDetailsQuery123();
        try {
            DefaultTableModel defaultTableModel = (DefaultTableModel) jTable1.getModel();

            int index = jTable1.getSelectedRow();

            CustomerDetails customerDetails = new CustomerDetails();

            customerDetails.setId(Integer.parseInt(jTable1.getValueAt(index, 0).toString()));

            int confirm = JOptionPane.showConfirmDialog(null, MessageFormat.getMessage("Are you sure of deleting this guest?"));

            if (confirm == JOptionPane.YES_OPTION) {
                warehoseDetailsQuery.deleteFromCustomerDetails(customerDetails);

                JOptionPane.showMessageDialog(null, MessageFormat.getMessage("CustomerDetails Deleted successfully"));
                customerDetails = new CustomerDetails();
                loadWarehouse();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, MessageFormat.getMessage("Please select the employee you want to delete"), "Error Message", JOptionPane.ERROR_MESSAGE);
        }

    }

