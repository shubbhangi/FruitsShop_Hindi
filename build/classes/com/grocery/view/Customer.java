   private void showInformationTable(JComboBox component, JTable table) {
        Session session = HibernateUtil.getSessionFactory().openSession();
        try {
            String custName = component.getSelectedItem().toString();
            String query = "from customer_master where name = '" + custName + "'";

            List<CustomerDetails> list = new ArrayList<>();
            Query q = session.createQuery(query);
            list = q.list();

            for (CustomerDetails customerDetails : list) {

                table.setValueAt(customerDetails.getId(), 0, 1);
                table.setValueAt(customerDetails.getName(), 1, 1);

                table.setValueAt(customerDetails.getAddress(), 2, 1);
                table.setValueAt(customerDetails.getContact(), 3, 1);

//                table.setValueAt(customerDetails.getTdsNumber(), 4, 1);
                //table.setValueAt("SRID",5,1);
               // table.setValueAt(customerDetails.getBalance(), 5, 1);

                //table.setValueAt(customerDetails.getGstNumber(), 6, 1);

            }
            session.close();
        } catch (Exception e) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, e);
        }

   }
    
