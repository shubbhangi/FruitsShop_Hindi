    private void updateEmployee() {
        if (jPasswordFieldNewPassword.getText().trim().isEmpty() || jPasswordFieldConfirmPassword.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "None of the fields should be empty");
            jPasswordFieldConfirmPassword.setText("");
            jPasswordFieldNewPassword.setText("");
            return;
        }

        if (jPasswordFieldNewPassword.getText().equals(jPasswordFieldConfirmPassword.getText())) {
            int confirm = JOptionPane.showConfirmDialog(null, "Are you sure of resetting the password?");
            if (confirm == JOptionPane.YES_OPTION) {
                EmployeeMaster employeeMaster = new EmployeeMaster();
                employeeMaster = this.employeeMaster;
                employeeMaster.setPassword(jPasswordFieldNewPassword.getText());

                EmployeeQuery employeeQuery = new EmployeeQuery();
                employeeQuery.updatePassword(employeeMaster);

                JOptionPane.showMessageDialog(null, "Password updated successfully");
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Passwords entered in both the fields don't match", "Error Message", JOptionPane.ERROR_MESSAGE);
            jPasswordFieldConfirmPassword.setText("");
            jPasswordFieldNewPassword.setText("");
            jPasswordFieldNewPassword.requestFocus();
        }
    }
